      Ce projet, encadré par Marc-Michel Corsini et Camille Jeunet, vient s’inscrire dans un projet de recherche étudiant l’acceptabilité des Interfaces Cerveau-Ordinateur (ICO) en tant qu’outil de rééducation post-Accident Vasculaire Cérébral (AVC).<br>
      <br><br>
      Notre problématique était : « identifier les facteurs d’acceptabilité des interfaces cerveau-ordinateur pour pouvoir, par la suite, éventuellement changer le regard de la population et/ou les prendre en compte dans la conception de procédures d'entraînement ICO post-AVC. » <br><br>
      Pour construire ce projet, il nous a tout d’abord fallu déterminer ce qu’est une ICO :
      <center><i>Une ICO est un système de communication entre un individu et une machine basée sur son activité cérébrale. </i></center>
      Nous avons ensuite cherché à montrer quel est l’intérêt d’utiliser ces interfaces, et ceux, particulièrement dans le cadre de la rééducation post-AVC. En effet, cette technologie semble prometteuse dans ce contexte, notamment pour apprendre aux patients à regagner une mobilité perdue.  <br>
      Nous avons par la suite lu des articles afin de faire une revue des facteurs d’acceptabilité des ICO. Les facteurs d’acceptabilité sont les caractéristiques que présente l’individu qui lui permettraient un meilleur rapport avec l’interface (on parle aussi d’utilisabilité). Ces facteurs vous ont été présentés dans la rubrique Facteurs d’acceptabilité des ICO en parallèle des hypothèses que nous avons pu émettre sur leurs interactions. Nous avons noté cinq grandes catégories : les critères socio-démographiques, les expériences personnelles de vie, les facteurs cognitifs et psychologiques, les relations de l'individu avec les nouvelles technologies et le rapport aux soins.<br><br>
      Afin d’étudier les facteurs identifiés, nous avons construit un questionnaire afin de regarder quelles seraient les personnes prêtes à utiliser ce genre de thérapie et identifier leurs similitudes. Pour ce faire, nous avons appris à manipuler la plateforme d’enquêtes Qualtrics.  Nous avons sélectionné une trentaine de facteurs. Pour les étudier, nous nous sommes intéressés aux normes de l’INSEE, et à des tests psychotechniques tel que le SEI de Coopersmith ou le STAI-Y de Spielberger.<br>
      De plus, ce questionnaire a été soumis à un comité d’éthique et de recherche. <br><br>
      Les résultats obtenus avec nos analyses statistiques sur 66 individus ont permis d'identifier des modalités qui se ressemblent. Nous remarquons que les plus jeunes « 18-24 ans », souvent étudiants, et qui ne font pas de sports, sont les plus anxieux et se soucient le plus de l’aspect esthétique de l’outil thérapeutique. Nous remarquons que les retraités de 65 ans et plus présentent les caractéristiques contraires : ils ne sont pas anxieux, font du sport et ne soucient pas de l’aspect esthétique de l’outil thérapeutique. De plus les jeunes auraient besoin de plus d’explications vis à vis d’une thérapie que leurs ainés qui ne la jugerait pas nécessaire. La notion de "voir des résultats rapidement" est plus importante chez les plus jeunes. L’analyse n’a pas pu montrer les groupes qui refuseraient d’essayer cette thérapie par ICO, ni ce qui ne la jugerait pas utile par manque de représentant. <br>
      En ce qui concernent notre hypothèse pays, nous n’avons pas pu l’étudier n’ayant qu’un seul représentant en dehors de la France, et qui présentait un comportement similaire aux français. De même, nous n’avons pas pu étudier l’impact de la dépression sur les autres variables étant donné qu’aucun de nos participants présentait de signe dépressif. <br>
      Nous notons que ces similitudes sont sans doute le fruit de la sélection de l’échantillon. Puisque celui-ci s’est fait dans nos cercles familiaux et amicaux. Par exemple, la catégorie correspondant à "agglomération de plus de 20 000 à 50 000 habitants" est sur-représentée car l'une d'entre nous est originaire d'une ville de cette taille. De plus, nous connaissons majoritairement des personnes ayant vécu dans des agglomérations de tailles importantes. Nous remarquons aussi que l'ensemble de notre échantillon accepterait d'effectuer la thérapie. <br><br>
      Nous aurions souhaité les interactions des facteurs étudiés mais nous n’en avons pas eu le temps. <br><br>
      Pour réaliser ce projet, nous étions en contact avec David Gasq, médecin spécialiste en médecine physique et de réadaptation au CHU de Toulouse. Il a pu faire tester le questionnaire à une personne ayant subi un AVC, ainsi qu'a un ergothérapeute. Les perspectives futures de ce projet seraient de faire remplir le questionnaire à un groupe d'une trentaine de personnes incluant 10 à 15 sujets ayant subi un AVC et 10 à 15 personnes "saines".
