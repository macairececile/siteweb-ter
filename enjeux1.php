        <h5>Résumé</h5><br>
        <p class="excert">
          <i>L'utilisation des interfaces cerveau-utilisateur soulèvent plusieurs questions éthiques, notamment au niveau économique, au niveau thérapeutique et enfin, au niveau de sa finalité.</i>
        </p>
      </div>
      <div class="quotes">
        <p class="excert">
          En effet, aujourd'hui, les technologies n'échappent pas aux contraintes légales ou éthiques. <br />
          Les interfaces cerveau-ordinateur peuvent avoir un objectif de réhabilitation de fonctions perdues et/ou d'assistance technologique suite à la perte d'une fonction, <a href="bibliographie.php#vlek12">(Vlek et al., 2012)</a>. Cette technologie présente des problématiques éthiques liées à celle de la recherche médicale et de ses applications (notamment appareillage médical).
        </p>
        <p class="excert">
          Tout d'abord, la question économique est soulignée : est-ce que les interfaces cerveau-ordinateur connaîtront le même enjeu économique que l'industrie pharmaceutique avec potentiellement une inégalité d'accès aux thérapies et une recherche et développement plus poussée pour les pathologies communes à défaut des pathologies dites orphelines ? <a href="bibliographie.php#atthiah14">(Atthiah et al., 2014)</a>.
        </p>
        <p class="excert">
          Ensuite, les questions concernant l'adhésion à une étude sont étudiées <a href="bibliographie.php#vlek12">(Vlek et al., 2012)</a>. <br>
          Vlek et al., après l'étude de cas lock-in syndrome (individus ayant une paralysie qui limite la communication aux paupières et parfois aux yeux) soulèvent des questions autour du thème du consentement :  est-ce qu'un proche est la meilleure personne pour prendre une décision ? (biais affectif). En effet, c'est une problématique qui est résolue différemment selon les pays : certains laissent les proches prendre les décisions alors que d'autres font intervenir une tierce personne.
        </p>
        <p class="excert">
          Il est aussi noté qu'aux yeux du public, la dissociation entre recherche et thérapie est difficile. Ceci peut-être à l'origine d'un malentendu thérapeutique., c'est à dire d'une discordance entre le but du soigné (le soin, voire une amélioration de son état) et le but du soignant (la contribution à la recherche scientifique) dans le cadre des études interfaces cerveau-ordinateur. Une couverture médiatique positive d'une expérience peut participer à ce malentendu thérapeutique.
        </p>
        <p class="excert">
          En parallèle, nous pouvons remarquer que le public recevant cet instrument, est sujet à une sélection lors des études : il est préférable d'éviter certaines lésions tierces à celles étudiés, et les épileptiques sont exclus, à cause des signaux qu'ils émettent, semblables à ceux de la P300 (un des signaux pouvant être exploré avec les interfaces cerveau-ordinateur).
        </p>
        <p class="excert">
          Enfin, une notion importante est la finalité de l'utilisation de cette technologie : si elle peut causer une amélioration, elle peut aussi causer une dégradation de nos états, <a href="bibliographie.php#vlek12">(Vlek et al., 2012)</a>.
        </p>
        <p class="excert">
          A terme, les interfaces cerveau-ordinateur pourront être utilisées afin de décupler les capacités humaines : dans Ethical Issues in Brain - Computer Interface Research, Development, and dissemination <a href="bibliographie.php#vlek12">(Vlek et al., 2012) </a> Vlek et al. évoquent par exemple le cas d'une application de ce genre : un contrôleur de trafic aérien est équipé d'un outil neurotechnologique produisant un neurofeedback (c'est une interface cerveau-ordinateur!) afin d'améliorer son attention. Face à ces applications futuristes, des questions  concernant l'identité  et la vie privée (lecture des pensées, accès aux états psychologiques...) et de manipulation mentale sont soulevées, <a href="bibliographie.php#atthiah14">(Atthiah et al.,2014)</a>, <a href="bibliographie.php#vlek12">(Vlek et al., 2012)</a>, <a href="bibliographie.php#haselager09">(Haselager et al., 2009)</a>.
        </p>
