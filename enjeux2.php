        <h3>Qu'est-ce qu'un AVC ?</h3><br>
        <p class="excert">
          Les accidents vasculaires cérébraux (AVC) sont un déficit neurologique causés par l'apparition d'une hémorragie cérébrale ou d'un infarctus et touche plus d'1% de la population.
          Les séniors sont les personnes les plus touchées. En effet, l'âge moyen se situe autour de 65 ans. Mais un AVC peut survenir également chez des individus plus jeunes.
          L'AVC représente la première cause de handicap acquis de l'adulte, la deuxième cause de démence et la troisième cause de mortalité.
        </p>
      </div>
