      <div class="quotes">
        <h3>Conséquences</h3><br>
        <p class="excert">
          Lorsqu'une personne subit un AVC, les conséquences peuvent être multiples. En effet, lorsqu'une personne subit une hémorragie cérébrale, des parties du cerveau peuvent être endommagées et être à l'origine de pertes de facultés cognitives importantes.
          On peut prendre comme exemple les troubles de la mémoire, du langage (par exemple, l'aphasie qui correspond à la perte totale ou partielle de communiquer par le langage), de l'humeur, la dépression, le stress et des troubles moteurs.
          Cette personne peut également subir une paralysie d'une ou de plusieurs parties d'un seul côté du corps, on parle alors d'hémiplégie.
          <br><br>
          La rééducation post-AVC vise donc à rétablir les facultés cognitives perdues et la motricité.
          Elle va tout d'abord se porter sur la sensibilité de la peau, ainsi que la perception de la position du corps dans l'espace. Des exercices physiques peuvent être mis en place pour améliorer la condition physique de la personne, et réapprendre les mouvements moteurs (marcher, saisir un objet, ...).
        </p>
      </div>
      <div class="quotes">
        <h3>Les interfaces cerveau-ordinateur dans la rééducation post-AVC</h3><br>
        <p class="excert">
          Dans certains cas, les outils traditionnels utilisés dans la rééducation post-AVC sont insuffisants. On peut prendre le cas de personnes atteintes d'une paralysie partielle des membres supérieurs. Ces méthodes traditionnelles sont plus efficaces dans la rééducation des membres inférieurs (il est plus facile de réapprendre à marcher que de saisir un objet). <br><br>
          Il est donc intéressant de s'intéresser à de nouvelles technologies, proposant des méthodes nouvelles et innovantes pour, à terme, améliorer la qualité de vie de ces personnes. <br><br>
          Des tests sur les interfaces ont déjà été effectués et montre de réelles améliorations dans la vie du patient, notamment en contrôlant une prothèse de main, ou un fauteuil roulant motorisé, <a href="bibliographie.php#pichorri15">(Pichiorri, 2015)</a>, <a href="bibliographie.php#chaudhary16">(Chaudhary, 2016)</a>.
          Nous rappelons ici que la notion de performance des thérapies utilisant les interfaces cerveau-ordinateur est associée à la fiabilité mais aussi à la notion d'acceptabilité. En effet, il est pertinent d'étudier le cas précis de la rééducation post-AVC car les individus sont souvent stressés, et l'AVC touche principalement des personnes âgées. <br><br>
          <strong>Nous cherchons donc à identifier les facteurs d'acceptabilité des interfaces cerveau-ordinateur pour pouvoir, par la suite, éventuellement changer le regard de la population et/ou les prendre en compte dans la conception de procédures d'entraînement ICO post-AVC. </strong>
