							<p class="excert" style="text-align:justify">
								A cerebrovascular accident (or stroke) is a neurological deficit caused by a cerebral haemorrhage or an infarction which can affects more than 1% of the population.
								As part of post-stroke rehabilitation, different therapies are developed to allow one person to recover their lost abilities.
								In some cases, the customary means employed in post-stroke rehabilitation are inadequate.
								Some researchers have therefore taken an interest in new technologies such as brain-computer interfaces. As a matter of fact, a brain-computer interface refers to a communication system between an individual and a machine, based on their brain activity.
								Tests have been performed and have shown a real improvement in patients' lives. To enhance the performance of the analysis, studies have focused on its reliability.
								Yet, this notion of performance of brain-computer interfaces is linked to its reliability but also to acceptability factors.
								Acceptability factors refer to an individual's characteristics that would allow them to have a better relation with the interface (also known as usability).
								What are the features and characteristics of a person which would lead to greater acceptability in the brain-computer interface so that, in the long term, they can design more appropriate therapies and training procedures for computer interfaces?
								To answer this question, we identified and sorted the factors of acceptability, and made up a questionnaire that we shared. Subsequently, we led a descriptive and qualitative analysis of the data collected.
								Thanks to the responses of 66 individuals, we showed some similar modalities. For example, people between the ages of 18 and 25, who do not play sports, are more anxious and concerned about the aesthetics of the therapeutic tool, unlike people over 65 years old. We were unable to determine which groups would refuse to try the therapy, or what would not be useful for lack of representative.
								It would be interesting to spread the questionnaire to both subjects who experienced a stroke and others who did not to determine more precisely the impact of certain factors on acceptability.
