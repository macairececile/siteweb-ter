							<p class="excert" style="text-align:justify">Les accidents vasculaires cérébraux (AVC) sont un déficit neurologique causés par l'apparition d'une hémorragie cérébrale ou d'un infarctus et touche plus d'1% de la population. Dans le cadre de la rééducation post-AVC, différentes thérapies sont mises en place pour permettre à une personne de retrouver ses facultés perdues. Dans certains cas, les outils traditionnels utilisés dans la rééducation post-AVC sont insuffisants.
								Des chercheurs se sont donc intéressés à de nouvelles technologies, comme les interfaces cerveau-ordinateur. En effet, une interface cerveau-ordinateur désigne un système de communication entre un individu et une machine, basée sur son activité cérébrale. Actuellement, des tests sur ces interfaces ont été réalisés et montrent de réelles améliorations dans la vie du patient. Pour améliorer la performance dans l'outil, les études précédentes se sont centrées sur sa fiabilité. Or, cette notion de performance autour des interfaces cerveau-ordinateur est associée à la fiabilité mais également à d'autres facteurs, comme les facteurs d'acceptabilité. Les facteurs d’acceptabilité désignent les caractéristiques que présente l’individu qui lui permettraient un meilleur rapport avec l’interface (on parle aussi d’utilisabilité). Quels sont donc les traits et les caractéristiques d'une personne qui entraineraient une meilleure acceptabilité dans l'interface cerveau-ordinateur pour pouvoir, à terme, concevoir des thérapies et des procédures d'entrainement interfaces ordinateur plus adaptées ?
								Pour répondre à cette problématique, nous avons identifié et catégorisé les facteurs, puis construit et diffusé un questionnaire. Nous avons, par la suite, réalisé une analyse descriptive et qualitative des données récoltées.
								Grâce aux réponses de 66 individus, nous avons montré certaines modalités similaires. Par exemple, les personnes âgées entre 18 et 25 ans, qui ne font pas de sports, sont plus anxieux et soucieux de l'esthétique de l'outil thérapeutique, à l'inverse des personnes âgées de plus de 65 ans.
								Nous n'avons pas pu déterminer les groupes qui refuseraient d’essayer cette thérapie par ICO, ni ce qui ne la jugerait pas utile par manque de représentants.
								Il serait intéressant de diffuser le questionnaire entre deux groupes : des sujets ayant subi un AVC et d'autres non pour déterminer plus précisément l'impact de certains facteurs sur l'acceptabilité.</p>
