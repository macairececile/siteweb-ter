                <h5>Résumé</h5><br>
                <p class="excert">
                  <i>Les interfaces cerveau-ordinateur sont issues de la recherche interdisciplinaire et sont appliquées à plusieurs domaines. Néanmoins, cela reste toujours une technologie de laboratoire et ces interfaces sont utilisées dans un contexte spécifique (thérapies, ...).</i>
                </p>
              </div>
              <div class="quotes">
                <p class="excert">
                  Les interfaces cerveau-ordinateur sont des outils issus de la recherche interdisciplinaire qui mettent en relation l'intelligence artificielle, les neurosciences, mais aussi la psychologie et dont les applications sont variées.
                </p>
                <p class="excert">
                  Le principal domaine d'application est le médical.<br />
                  On peut prendre comme exemple une interfaces cerveau-ordinateur basée sur EEG (électroencéphalogramme) utilisée pour contrôler des prothèses de main ou de bras de personnes complètement paralysées, <a href="bibliographie.php#guger99">(Guger et al., 1999)</a>. Ou encore des éditeurs de textes pour permettre à un patient de communiquer avec le monde extérieur en sélectionnant sur un écran des lettres pour former des mots puis ensuite des phrases.
                </p>
                <p class="excert">
                  Mais ces interfaces commencent à s'implanter dans le domaine de la réalité virtuelle et des jeux vidéos dans un but non thérapeutique. <br/>
                  Le jeu "Brain Invaders" est un jeu vidéo inspiré de "Space Invaders", dont le but est de détruire des aliens à l'aide d'un canon laser. Le système proposé utilise uniquement la pensée de l'utilisateur. Le joueur, équipé d'un casque EEG, se focalise sur une grille 6 sur 6 de 36 cibles possibles. Lorsque l'alien doit être détruit, il clignote. Le joueur va porter son attention sur celui-ci. L'interface cerveau-ordinateur se chargera d'interpréter les signaux neuronaux du joueur pour effectuer une action spécifique, dans ce cas, déterminer sur quel cible le joueur a porté son intention pour que le système le détruise, <a href="bibliographie.php#korczowski16">(Korczowski et al., 2016)</a>.
                </p>
              </div>
              <div class="quotes">
                <h3>Une technologie de laboratoire</h3><br>
                <p class="excert">
                  Néanmoins, les interfaces cerveau-ordinateur constituent toujours une technologie de laboratoire.
                  De ce fait, l'application des interfaces cerveau-ordinateur comme outil de rééducation ou de thérapie n'est pas possible en dehors du contexte de recherche.
                  La difficulté de manipulation de certaines variables est à l'origine de ce <i>cloîtrage</i> dans les laboratoires.
                </p>
                <p class="excert">
                  En effet, les conditions de laboratoires sont nécessaires pour la manipulation de certains facteurs <a href="bibliographie.php#vlek12">(Vlek et al., 2012)</a> comme notamment le rapport signal sur bruit (témoin de la transmission de l'information de l'individu à la machine).
                  Ce signal est corrélé à la vitesse et à la fiabilité du système et est dépendant des variabilités inter-individuelles.
                  Un moyen d'agir sur l'amélioration du rapport signal sur bruit est l'utilisation d'algorithmes afin de maximiser ce rapport,<a href="bibliographie.php#lotte16">(Lotte et Congedo, 2016)</a>.
                  Pour cela, il faut mettre la réponse électrophysiologique étudiée (spécifique à la tâche) en valeur par rapport aux autres "bruits" émis par le cerveau.
                </p>
                <p class="excert">
                  Il nous faudra aussi remarquer que la variabilité inter-individuelle <a href="bibliographie.php#vlek12">(Vlek et al., 2012)</a> et intra-individuelle au cours du temps, ceux-ci rendent difficile la prédiction de la performance.
                  Afin de parer ces variabilités, le sujet est <i>entraîné</i>, c'est à dire qu'il est familiarisé à l'outil.
                  Notons ici que l'entraînement utilisé est le même depuis les années 80-90 et est peu agréable pour l'utilisateur, du fait de sa redondance : l'exercice est répétitif et la récompense peu gratifiante.
                  En effet, pendant de multiples sessions, le sujet réalise des tâches d'imagerie mentale (il imagine des mouvements de la main gauche, de rotation mentale, de soustraction mentale) sur une interface peu plaisante, et reçoit de la part de l'ordinateur une barre bleue proportionnelle à la performance comme stimulus de récompense.
                  <center><iframe width="560" height="315" src="https://www.youtube.com/embed/5cbNxnfJOwA" frameborder="0" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe></center><br>
                  Nous pouvons imaginer que l'affect émotionnel développé lors de la tâche d'entraînement ait une répercussion sur les performances finales.
                  Cet affect pourrait être amélioré par une récompense de type soutien émotionnel, récompense reconnue pour améliorer la créativité et l'apprentissage, <a href="bibliographie.php#isen87">(Isen et al., 1987)</a>.
                </p>
                <p class="excert">
                  En bref, l'amélioration de l'interprétation des signaux cérébraux via des algorithmes de plus en plus performants a permis d'améliorer la fiabilité des interfaces cerveau-ordinateur.
                  La fiabilité sera ici définie comme la capacité de fonctionnement de l'outil.
                </p>
                <p class="excert">
                  Actuellement, dans le but d'obtenir les meilleurs performances, les deux champs suivants sont essentiellement étudiés  <a href="bibliographie.php#bos11">(Bos et al., 2011)</a>, <a href="bibliographie.php#gurkok11">(Gürkök et al., 2011)</a>, <a href="bibliographie.php#van11">(Van de Laar et al., 2011)</a> :
                </p>
                <p class="excert">
                  <strong>- l'utilisabilité de l'interface</strong>
                </p>
                <p class="excert">
                  <strong>- l'expérience de l'utilisateur</strong> (UX : user experience)
                </p>
                <p class="excert">
                  L'utilisabilité d'une interface cerveau-ordinateur peut être mesurée par la vitesse et la précision  de l'interface, <a href="bibliographie.php#van11">(Van de Laar et al., 2011)</a>.
                  D'après les faits précédents, l'utilisabilité est corrélée à la fiabilité.
                </p>
                <p class="excert">
                  L'expérience de l'utilisateur est décrite par l'acceptabilité des interfaces cerveau-ordinateur et le plaisir ressenti par l'utilisateur, <a href="bibliographie.php#van11">(Van de Laar et al., 2011)</a>.
                </p>
                <p class="excert">
                  L'enjeu de l'amélioration des performances est lié au fait que de 10 à 30% des utilisateurs sont incapables de manipuler l'interface, <a href="bibliographie.php#jeunet16">(Jeunet et al., 2016)</a>, <a href="bibliographie.php#allison10">(Allison et Neuper, 2010)</a>.
                  Or, la notion de fiabilité est souvent celle travaillée lors des études interfaces cerveau-ordinateur cherchant à améliorer les performances.
                  Cependant, d'autres facteurs peuvent être utilisés pour améliorer ces performances, ce sont les facteurs d'acceptabilité.
                  En effet, d'après Zander et Kothe, les interfaces cerveau-ordinateur "tireraient bénéfice des informations sur l'état de l'utilisateur", <a href="bibliographie.php#zander11">(Zander et Kothe, 2011)</a>.
                  Les facteurs d'acceptabilité sont des facteurs relatifs à l'utilisateur : ses caractéristiques et son rapport à la machine.
                </p>
                <p class="excert">
                  Ces facteurs d'acceptabilité se voient moins étudiés que l'interface machine et la notion de fiabilité adjacente marquant ici une césure entre les neurosciences, la psychologie et l'informatique.
                  Effectivement, les études réalisées en psychologie, en informatique, ergonomie, ... ont pour unique but d'améliorer la fiabilité de ces interfaces.
                  Hors, la performance liée à l'utilisation d'interfaces cerveau-ordinateur dépend de la fiabilité, mais aussi d'autres facteurs comme l'acceptabilité.
                  Une identification et un contrôle des facteurs d'acceptabilité aux interfaces cerveau-ordinateur pourraient permettre une amélioration de la performance générale.
                  En effet, de nombreux facteurs étudiés peuvent être des catalyseurs ou des inhibiteurs de ce type de relation homme-machine.
                </p>
